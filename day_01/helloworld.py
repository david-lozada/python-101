print('David')             # addition(+)
print('Lozada')             # subtraction(-)
print('Venezuela')             # multiplication(*)
print('I am enjoying 30 days of python')             # division(/)
print(3 ** 4)            # exponential(**)
print(3 % 4)             # modulus(%)
print(3 // 4)            # Floor division operator(//)

# Checking data types
print(type(10))          # Int
print(type(9.8))        # Float
print(type(3.14))      # Complex number
print(type(4 - 4j))  # String
print(type(['Asabeneh', 'Python', 'Finland']))   # List
print(type('David')) # Dictionary
print(type('Lozada'))    # Set
print(type('Venezuela'))    # Tuple